window.onload=function(){
		$('#discount-info').click(function(){
			$('.Information').css('display','block')
			$('#main').css('filter','blur(10px)')
		})
		$('#close').click(function(){
			$('.Information').css('display','none')
			$('#main').css('filter','none')
		})
		var goods=document.getElementById('goods'),
		ratings=document.getElementById('ratings'),
		seller=document.getElementById('seller');
		goods.style.color='red';
		var arr=[goods,ratings,seller]
		for(var i=0;i<3;i++){
			arr[i].onclick=function(){
				for(var j=0;j<3;j++){
					arr[j].style.color='rgb(77,85,93)';
				}
				this.style.color='red';
			}
		}
		$('#goodsInfo').scroll(function(){
			if($('#goodsInfo').scrollTop()<50){
				$('#Nav1').css('backgroundColor','white');
				$('#Nav2').css('backgroundColor','');
				$('#Nav3').css('backgroundColor','');
				$('#Nav4').css('backgroundColor','');
			}
			if($('#goodsInfo').scrollTop()>50){
				$('#Nav1').css('backgroundColor','');
				$('#Nav2').css('backgroundColor','white');
				$('#Nav3').css('backgroundColor','');
				$('#Nav4').css('backgroundColor','');
			}
			if($('#goodsInfo').scrollTop()>330){
				$('#Nav2').css('backgroundColor','');
				$('#Nav3').css('backgroundColor','white');
				$('#Nav1').css('backgroundColor','');
				$('#Nav4').css('backgroundColor','');
			}
			if($('#goodsInfo').scrollTop()>400){
				$('#Nav3').css('backgroundColor','');
				$('#Nav4').css('backgroundColor','white');
				$('#Nav1').css('backgroundColor','');
				$('#Nav2').css('backgroundColor','');
			}
		})
		$('#Nav2').click(function(){
			$('#Nav1').css('backgroundColor','');
			$('#Nav2').css('backgroundColor','white');
			$('#Nav3').css('backgroundColor','');
			$('#Nav4').css('backgroundColor','');
			document.getElementById('goodsInfo').scrollTo(0,100)
		})
		$('#Nav3').click(function(){
			$('#Nav1').css('backgroundColor','');
			$('#Nav2').css('backgroundColor','');
			$('#Nav3').css('backgroundColor','white');
			$('#Nav4').css('backgroundColor','');
			document.getElementById('goodsInfo').scrollTo(0,400)
		
		})
		$('#Nav4').click(function(){
			$('#Nav1').css('backgroundColor','');
			$('#Nav2').css('backgroundColor','');
			$('#Nav3').css('backgroundColor','');
			$('#Nav4').css('backgroundColor','white');
			document.getElementById('goodsInfo').scrollTo(0,500)
		})
		$('#Nav1').click(function(){
			$('#Nav1').css('backgroundColor','white');
			$('#Nav2').css('backgroundColor','');
			$('#Nav3').css('backgroundColor','');
			$('#Nav4').css('backgroundColor','');
			document.getElementById('goodsInfo').scrollTo(0,0)
		})
		$('.cart').click(function(){
			var timer=null;
			timer=setInterval(function(){
				$('#cartInfo').css('display','block');
				$("#cartInfo").animate({top:'-=200px'});
				$('.headerChange').addClass('AppChange');
				$('.contentChange').addClass('mainChange');
			clearInterval(timer)},100);
			$('.cart').addClass('disable');
			
		})
		$('.back').click(function(){
			$('.cart').removeClass('disable');
			$("#cartInfo").animate({top:'+=200px'});
			$('.headerChange').removeClass('AppChange');
			$('.contentChange').removeClass('mainChange');
		})
	}